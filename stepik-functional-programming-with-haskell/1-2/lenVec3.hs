-- Реализуйте функцию трех аргументов lenVec3,
-- которая вычисляет длину трехмерного вектора.
-- Аргументы функции задают декартовы координаты конца вектора,
-- его начало подразумевается находящимся в начале координат.
-- Для извлечения квадратного корня воспользуйтесь функцией sqrt,
-- определенной в стандартной библиотеке.

-- lenVec3 x y z = sqrt ( x ^ 2 + y ^ 2 + z ^ 2 )
lenVec3 x y z = sqrt $ x ^ 2 + y ^ 2 + z ^ 2
